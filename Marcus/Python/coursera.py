# KEY TERMS TO REMEMBER # Part 1 of our learning terminology study guide #
#
# Programming code - is a set of written instructions, guided by rules, using a computer programming language. 
#
# Programming languages - Programming languages are similar to human spoken languages in that they both use syntax and semantics. Programming languages are used to write computer programs. 
#
# Syntax - Syntax is a set of rules for how statements are constructed in both human and computer languages. This includes rules for the order of elements in programming instructions, as well as use of special characters and their placements in statements. 
#
# Semantics - Semantics refers to the intended meaning or effect of statements, or collections of words, in both human and computer languages. Semantic errors are also referred to as logical errors. 
#
# Computer Program - A computer program is a step-by-step list of instructions that a computer follows to reach an intended goal. It is important to be clear and precise about the actions a computer program is supposed to perform because computers will do exactly what they are instructed to do. 
#
# Script - Scripts are usually shorters and less complex than computer programs. Scripts are often used to automate specific tasks. However, they can be used for complex tasks if needed. Scripts are often written by IT Pros, but anyone can learn to create and write scripts as well. 
#
# Automation - Automation is used to replace a repetitive manual step with one that happens automatically. 
#
# Output - Output is the end result of a task performed by a function or computer program. Output can include a single value, a report, entires into a database, and more. 
#
# Input - Input is information that is provided to a program by the end user. Input can be text, voice, images, biometrics, and more. 
#
# Functions - A function is a reusable block of code that performs a specific task. 
#
# Variables - Variables are used to temporarily store changeable values in programming code. 
